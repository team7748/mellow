/**
 * Enhancement data for existing vocabulary.json words.
 * Applied at load time to add category, icon, level, and standardized POS fields.
 */
export type VocabEnhancement = {
  category: VocabCategory[]
  subcategory: string
  level: VocabLevel
  partOfSpeechStandard: PartOfSpeech
  icon: string
  fallbackIcon: string
  assetType: AssetType
  assetSource: AssetSource
}

/** Maps partOfSpeech abbreviations from the original data to standard values */
export function normalizePartOfSpeech(pos: string): PartOfSpeech {
  const normalized = pos.trim().toLowerCase()
  if (normalized.startsWith("v")) return "verb"
  if (normalized.startsWith("n")) return "noun"
  if (normalized.startsWith("adj")) return "adjective"
  if (normalized.startsWith("adv")) return "adverb"
  if (normalized.startsWith("prep")) return "preposition"
  if (normalized.startsWith("pron")) return "pronoun"
  if (normalized.startsWith("conj")) return "conjunction"
  return "noun"
}

/** Maps CEFR level to simplified VocabLevel */
export function cefrToLevel(cefr: string): VocabLevel {
  if (cefr === "A1") return 1
  if (cefr === "A2") return 2
  return 3
}

